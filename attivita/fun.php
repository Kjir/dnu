<h1>Rieducazione funzionale</h1>
La rieducazione funzionale in acqua unisce i vantaggi dell’ambiente liquido alla possibilità di iniziare
precocemente il trattamento di recupero dall' infortunio. L'acqua consente un’ottimizzazione del
lavoro che porta a ridurre i tempi di recupero ed a limitare le possibilità di comparsa di patologie
secondarie conseguenti ai carichi di lavoro eseguiti fuori dall’acqua. Il programma riabilitativo
verrà stilato sulla base delle esigenze di ogni singola persona e sarà seguito dalla nostra equipe
comprensiva di un esperto di rieducazione funzionale, e di uno psicologo, che farà da supporto al
paziente nella gestione delle problematiche legate all’infortunio e al recupero.

