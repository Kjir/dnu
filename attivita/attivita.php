<h1>Attività</h1>
La Ducato Nuoto Urbino promuove diverse attività adatte ad ogni età e abilità natatorie.<br>
In particolare vengono proposti:
<ul>
    <li>Scuola nuoto adulti</li>
    <li>Scuola nuoto bambini</li>
    <li>Acquafitness</li>
    <li>Rieducazione funzionale</li>
    <li>Nuoto libero</li>
    <li>Nuoto agonistico</li>
</ul>
