<h1>Nuoto libero</h1>
<h4>NUOTO LIBERO, PERCHE'</h4>
Il nuoto libero si configura come l'attività di libero accesso alla struttura. Adatta a chi necessità di flessibilità di orari e
giorni, permette al cittadino di praticare un'attività secondo le proprie preferenze e necessità. Ci si potrà rilassare,
divertire, imparare da autodidatti, socializzare. Sempre in sicurezza con la supervisione continua di un assistente bagnanti.
Indicato anche a chi volesse integrare con attività libera un corso frequentato contemporaneamente.

